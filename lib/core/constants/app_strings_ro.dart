  /// Format relative time in Romanian
  static String formatRelativeTime(int value, String unit) {
    if (value == 1) {
      switch (unit) {
        case 'day':
          return '1 $day în urmă';
        case 'week':
          return '1 $week în urmă';
        case 'month':
          return '1 $month în urmă';
        case 'year':
          return '1 $year în urmă';
        default:
          return '$value $unit';
      }
    } else {
      switch (unit) {
        case 'day':
          return '$value $daysAgo';
        case 'week':
          return '$value $weeksAgo';
        case 'month':
          return '$value $monthsAgo';
        case 'year':
          return '$value $yearsAgo';
        default:
          return '$value $unit';
      }
    }
